public class ProvinceDoctor {
    private Long doctorId;
    private Doctor.Doctor_Type doctorType;
    private Doctor.DoctorAppointStatus status;
    private String province;
    private String city;
    private Long total;

    public ProvinceDoctor(Long doctorId,
                          Doctor.Doctor_Type doctorType,
                          Doctor.DoctorAppointStatus status,
                          String province,
                          Long total) {
        this.doctorId = doctorId;
        this.doctorType = doctorType;
        this.status = status;
        this.province = province;
        this.total = total;
    }

    public ProvinceDoctor(Long doctorId,
                          Doctor.Doctor_Type doctorType,
                          Doctor.DoctorAppointStatus status,
                          String province,
                          String city,
                          Long total) {
        this.doctorId = doctorId;
        this.doctorType = doctorType;
        this.status = status;
        this.province = province;
        this.city = city;
        this.total = total;
    }

    public ProvinceDoctor(String province, String city, Long total) {
        this.province = province;
        this.city = city;
        this.total = total;
    }

    public ProvinceDoctor(String province, Long total) {
        this.province = province;
        this.total = total;
    }

    public ProvinceDoctor() {

    }

    public String getProvince() {
        return province;
    }

    public void setProvince(String province) {
        this.province = province;
    }

    public Long getTotal() {
        return total;
    }

    public void setTotal(Long total) {
        this.total = total;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public Long getDoctorId() {
        return doctorId;
    }

    public void setDoctorId(Long doctorId) {
        this.doctorId = doctorId;
    }

    public Doctor.Doctor_Type getDoctorType() {
        return doctorType;
    }

    public void setDoctorType(Doctor.Doctor_Type doctorType) {
        this.doctorType = doctorType;
    }

    public Doctor.DoctorAppointStatus getStatus() {
        return status;
    }

    public void setStatus(Doctor.DoctorAppointStatus status) {
        this.status = status;
    }
}
