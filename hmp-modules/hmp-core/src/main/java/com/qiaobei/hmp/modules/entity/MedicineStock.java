package com.qiaobei.hmp.modules.entity;

import java.util.Date;

/**
 * Created by Administrator on 2017/1/3 0003.
 */
public class MedicineStock {
    private Boolean HaveManager;    //是否有在库存管理
    private Date validityDate;      //药品综合有效期
    private Boolean isExpire;       //是否过期
    private Double stockNum;        //剩余库存
    private String barCode;         //条形码
    private Long companyId;         //公司id
    private String companyName;     //公司名


    public Date getValidityDate() {
        return validityDate;
    }

    public void setValidityDate(Date validityDate) {
        this.validityDate = validityDate;
    }

    public Boolean getExpire() {
        return isExpire;
    }

    public void setExpire(Boolean expire) {
        isExpire = expire;
    }

    public Double getStockNum() {
        return stockNum;
    }

    public void setStockNum(Double stockNum) {
        this.stockNum = stockNum;
    }

    public Boolean getHaveManager() {
        return HaveManager;
    }

    public void setHaveManager(Boolean haveManager) {
        HaveManager = haveManager;
    }

    public String getBarCode() {
        return barCode;
    }

    public void setBarCode(String barCode) {
        this.barCode = barCode;
    }

    public Long getCompanyId() {
        return companyId;
    }

    public void setCompanyId(Long companyId) {
        this.companyId = companyId;
    }

    public String getCompanyName() {
        return companyName;
    }

    public void setCompanyName(String companyName) {
        this.companyName = companyName;
    }
}
